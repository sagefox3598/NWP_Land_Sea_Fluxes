def load_areas(path=default_flow_path):
    return pd.read_csv(path).set_index("river_name")["total_area"].to_dict()

def load_chem_dfs(path=default_chemistry_path):
    with pd.ExcelFile(path) as DATA:
        chem_dfs = {
            riv: pd.read_excel(
                    DATA,
                    riv,
                    skiprows=range(2),
                    comment="#"
                )
            for riv in rivers
        }
        detection_limits = pd.read_excel(DATA, "detection_limits", index_col="Chem")["Limit"]
    # Need to get rid of replicates
    for chem, df in chem_dfs.items():
        df.set_index(pd.to_datetime(df["Date"]).rename("date_idx"), inplace=True)
        df["Date"] = df["Date"].astype("str")
        df.drop(columns=["Team", "Site", "replicate"], errors="ignore", inplace=True)

        # Mean same dates (replicates together... dropping the "object" columns too... could cause b.d. to be dropped? FIXME
        #chem_dfs[chem] = df.loc[:, df.dtypes != "str"].groupby("Date").mean()
        chem_dfs[chem] = df
        
        # If we can modify the process a bit... maybe replace b.d. values with -0
    
    rng_df = pd.DataFrame(
        columns=chemicals,
        index=pd.MultiIndex.from_product([rivers, ["start", "end"]], names=("river", "which"))
    )
    inner_outer = pd.DataFrame(
        columns=["inner", "outer"],
        index=pd.MultiIndex.from_product([rivers, ["start", "end"]], names=("river", "which"))
    )
    for riv in rivers:
        for chem in chemicals:
            idx = chem_dfs[riv][chem].dropna().index
            rng_df.loc[(riv, "start"), chem] = idx[0]
            rng_df.loc[(riv, "end"), chem] = idx[-1]
        
        inner_outer.loc[(riv, "start"), "inner"] = rng_df.loc[(riv, "start")].min()
        inner_outer.loc[(riv, "end"), "inner"] = rng_df.loc[(riv, "end")].max()
        inner_outer.loc[(riv, "start"), "outer"] = rng_df.loc[(riv, "start")].max()
        inner_outer.loc[(riv, "end"), "outer"] = rng_df.loc[(riv, "end")].min()

        assert inner_outer.at[(riv, "start"), "inner"] < inner_outer.at[(riv, "end"), "inner"] 
        # in case there is no overlap between some of the dates of the chem samples
        assert inner_outer.at[(riv, "start"), "outer"] < inner_outer.at[(riv, "end"), "outer"]
        # should be logically unnecessary but whatever
    
    rng_df = pd.concat([rng_df, inner_outer], axis=1)
    
    return chem_dfs, detection_limits, rng_df



