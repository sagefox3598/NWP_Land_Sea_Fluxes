# Generate a chemistry and info dataframe to be passed to the R process
# Note: outliers_removed is a mutable list to keep a log of all the outliers removed
def format_wdf(riv, chem,
               flow_df, chem_dfs, detection_limits,
               areas=areas, outliers_removed=outliers_removed):
    
    wdf = chem_dfs[riv][["Date", chem]]
    wdf["remarks"] = np.where((wdf[chem] == "b.d."), "<", "NA") # for b.d. in remarks
    print("Amt b.d.: ", (wdf["remarks"] == "<").sum())
    wdf[chem] = wdf[chem].replace("b.d.", detection_limits[chem]) # replce b.d. with the detection limits
    wdf[chem] = wdf[chem].replace([0, "", None, np.nan], "NA")
    print("Amt NA: ", (wdf[chem] == "NA").sum())
    wdf = wdf[wdf[chem] != "NA"]  # Remove rows with "NA" in the chemical concentration column
    
    mean, std = wdf[chem].mean(), wdf[chem].std()
    
    ###### NOTE: THIS IS WHERE OUTLIER THRESHOLDS ARE SET IN TERMS OF STANDARD DEVIATION.
    ###### TO AVOID REMOVING ANY OUTLIERS, SET THE LIMITS TO A HIGH NUMBER TIMES THE STANDARD DEVIATION
    ###### THIS WAS PREVIOUSLY SET TO 3*SD, BUT UPDATED TO 10*SD ON APRIL 10, 2025
    
    lim1 = mean - 10*std
    lim2 = mean + 10*std
    
    # Identify and log outliers before removing them
    outliers = wdf[(wdf[chem] > lim2) | (wdf[chem] < lim1)]
    for index, row in outliers.iterrows():
      outliers_removed.append({
          "River": riv,
          "Constituent": chem,
          "Date": index,
          "Value": row[chem],
          "Standard Deviations from Mean": (row[chem] - mean) / std
      })
    
    # Print the number of outliers found for debugging
    print(f"Outliers found for {riv} - {chem}: {len(outliers)}")
    
    # Remove outliers
    wdf = wdf[(wdf[chem] > lim1) & (wdf[chem] < lim2)]
    
    # Divide values by 1000 to convert from ug/L to mg/L
    wdf[chem] /= 1000
    
    #new TEMPORARY insert 2/5/2025 to trim conc data to available streamflow data date range
    flow_dates = flow_df.index  # Get available flow dates
    wdf = wdf[wdf["Date"].astype("str").isin(flow_dates)]  # Keep only concentration data for dates with flow
    #end insert
    assert flow_df[riv].notnull().all()
    
    # reorder the columns in wdf
    wdf = wdf[["Date", "remarks", chem]]

    # Because of rpy2 bugs or something idk, we need to convert the datetime to string first
    #wdf["Date"] = wdf["Date"].dt.strftime("%Y-%m-%d")
    
    return wdf

def save_outliers(path=default_out_data_path+"outliers_removed.csv"):
    outliers_df = pd.DataFrame(outliers_removed)
    outliers_df.to_csv(path, index=False)

